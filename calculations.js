export function add_two(num){
    return num + 2;
}


export function add(num1, num2) {
    return num1 + num2;

}
export function subt(num1, num2) {
    return num1 - num2;
}
export function mult(num1, num2) {
    return num1 * num2;
}
export function div(num1, num2) {
    return num1 / num2;
}
export function pyt(num1, num2) {
    return math.sqrt((num1 * num1) + (num2 * num2));
}
